package com.bw.ymy.mvptext1.Bean;

public class SingBean {

    private  String msg;
    private  int code;

    public SingBean(String msg, int code) {
        this.msg = msg;
        this.code = code;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }
}
